public class Spoiler {
    private String tipo, cor;

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    @Override
    public String toString() {
        return "Spoiler [tipo=" + tipo + ", cor=" + cor + "]";
    }


    
}
